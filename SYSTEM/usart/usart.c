/*
************************************************************
*	函数名称：	Usart1_Init
*
*	函数功能：	串口1初始化
*
*	入口参数：	baud：设定的波特率
*
*	返回参数：	无
*
*	说明：		TX-PA9		RX-PA10
************************************************************
*/
void Usart1_Init(unsigned int baud)
{

	GPIO_InitTypeDef gpioInitStruct;                           //定义GPIO结构体变量 
	USART_InitTypeDef usartInitStruct;                         //定义串口结构体变量
	NVIC_InitTypeDef nvicInitStruct;                           //???????????????
	
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);      //使能GPIOC的时钟
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_USART1, ENABLE);
	//RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA|RCC_APB2Periph_USART1,ENABLE);   //备用优化
	
	//PA9	TXD
	gpioInitStruct.GPIO_Mode = GPIO_Mode_AF_PP;                 //配置PA9为复用推挽输出     全双工
	gpioInitStruct.GPIO_Pin = GPIO_Pin_9;                       //配置TX引脚
	gpioInitStruct.GPIO_Speed = GPIO_Speed_50MHz;               //配置PA9速率
	GPIO_Init(GPIOA, &gpioInitStruct);                          //GPIO初始化函数
	
	//PA10	RXD
	gpioInitStruct.GPIO_Mode = GPIO_Mode_IN_FLOATING;            //配置PA10为浮空输入       全双工
	gpioInitStruct.GPIO_Pin = GPIO_Pin_10;                       //配置RX引脚
	gpioInitStruct.GPIO_Speed = GPIO_Speed_50MHz;                //配置PA10速率
	GPIO_Init(GPIOA, &gpioInitStruct);                           //GPIO初始化函数
	
	usartInitStruct.USART_BaudRate = baud;                       //波特率    形参
	usartInitStruct.USART_HardwareFlowControl = USART_HardwareFlowControl_None;		//无硬件数据流控制
	usartInitStruct.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	 //接收和发送模式
	usartInitStruct.USART_Parity = USART_Parity_No;							 //无校验
	usartInitStruct.USART_StopBits = USART_StopBits_1;					 //1位停止位
	usartInitStruct.USART_WordLength = USART_WordLength_8b;			 //8位数据位
	USART_Init(USART1, &usartInitStruct);                        //串口初始化函数
	
	USART_Cmd(USART1, ENABLE);														       //使能串口
	
	USART_ITConfig(USART1, USART_IT_RXNE, ENABLE);							 //使能接收中断
	
	nvicInitStruct.NVIC_IRQChannel = USART1_IRQn;                //?????????????????
	nvicInitStruct.NVIC_IRQChannelCmd = ENABLE;
	nvicInitStruct.NVIC_IRQChannelPreemptionPriority = 0;
	nvicInitStruct.NVIC_IRQChannelSubPriority = 2;
	NVIC_Init(&nvicInitStruct);

}

