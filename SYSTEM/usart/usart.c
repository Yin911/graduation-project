/*
************************************************************
*	函数名称：	Usart3_Init
*
*	函数功能：	串口3初始化
*
*	入口参数：	baud：设定的波特率
*
*	返回参数：	无
*
*	说明：		TX-PB10		RX-PB11
************************************************************
*/
void Usart3_Init(unsigned int baud)
{
	GPIO_InitTypeDef GPIO_InitStruct;                         //定义GPIO结构体变量   
	USART_InitTypeDef USART_InitStruct;                       //定义串口结构体变量
	NVIC_InitTypeDef nvicInitStruct;
	
	
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB,ENABLE);      //使能GPIOC的时钟
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART3,ENABLE); 
	
  GPIO_InitStruct.GPIO_Mode=GPIO_Mode_AF_PP;                //配置B10为复用推挽输出     全双工   
	GPIO_InitStruct.GPIO_Pin=GPIO_Pin_10;                     //配置TX引脚
	GPIO_InitStruct.GPIO_Speed=GPIO_Speed_50MHz;              //配置B10速率
	GPIO_Init(GPIOB,&GPIO_InitStruct);                        //GPIO初始化函数
	
	GPIO_InitStruct.GPIO_Mode=GPIO_Mode_IN_FLOATING;          //配置B11为浮空输入   
	GPIO_InitStruct.GPIO_Pin=GPIO_Pin_11;                     //配置RX引脚
	GPIO_InitStruct.GPIO_Speed=GPIO_Speed_50MHz;              //配置B11速率
	GPIO_Init(GPIOB,&GPIO_InitStruct);                        //GPIO初始化函数
	 
	
	USART_InitStruct.USART_BaudRate=baud;                      //波特率参数
  USART_InitStruct.USART_HardwareFlowControl=USART_HardwareFlowControl_None;   //无硬件数据流控制
	USART_InitStruct.USART_Mode=USART_Mode_Tx|USART_Mode_Rx;   //发送接收模式
	USART_InitStruct.USART_Parity=USART_Parity_No;             //无奇偶校验
	USART_InitStruct.USART_StopBits=USART_StopBits_1;          //停止位1位
	USART_InitStruct.USART_WordLength=USART_WordLength_8b;     //字长8位
	USART_Init(USART3,&USART_InitStruct);                      //串口初始化函数
	
	USART_Cmd(USART3,ENABLE);                                  //使能USART3
	
	USART_ITConfig(USART3, USART_IT_RXNE, ENABLE);						 //使能接收中断
	
	nvicInitStruct.NVIC_IRQChannel = USART3_IRQn;
	nvicInitStruct.NVIC_IRQChannelCmd = ENABLE;
	nvicInitStruct.NVIC_IRQChannelPreemptionPriority = 0;
	nvicInitStruct.NVIC_IRQChannelSubPriority = 1;
	NVIC_Init(&nvicInitStruct);

	
	
}

/*
************************************************************
*	函数名称：	USART1_IRQHandler
*
*	函数功能：	串口1收发中断
*
*	入口参数：	无
*
*	返回参数：	无
*
*	说明：		
************************************************************
*/
void USART1_IRQHandler(void)
{

	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET) //接收中断，清楚溢出错误
	{
		USART_ClearFlag(USART1, USART_FLAG_RXNE);
	}
	if(USART_GetFlagStatus(USART1, USART_FLAG_ORE) != RESET)
	{
		USART_ClearFlag(USART1, USART_FLAG_ORE); //清除溢出中断
	}

}






